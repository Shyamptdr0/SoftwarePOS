  // Validate license key format
  validateLicenseKeyFormat(licenseKey) {
    if (!licenseKey || typeof licenseKey !== 'string') {
      return false;
    }
    
    const parts = licenseKey.split('.');
    if (parts.length !== 2) {
      return false;
    }
    
    const [base64Part, checksum] = parts;
    
    // Check if base64 part is valid
    try {
      const decoded = Buffer.from(base64Part, 'base64');
      if (decoded.length === 0) {
        return false;
      }
    } catch (error) {
      return false;
    }
    
    // Check if checksum is valid (8 characters)
    if (!/^[a-f0-9]{8}$/i.test(checksum)) {
      return false;
    }
    
    // Verify checksum
    const calculatedChecksum = this.calculateChecksum(base64Part);
    return calculatedChecksum.toLowerCase() === checksum.toLowerCase();
  }

  // Batch generate licenses
  async batchGenerateLicenses(machineFingerprints, licenseType, options = {}) {
    const licenses = [];
    
    for (const fingerprint of machineFingerprints) {
      let license;
      
      switch (licenseType) {
        case 'trial':
          license = this.generateTrialLicense(fingerprint, options.trialDays || 30);
          break;
        case 'full':
          license = this.generateFullLicense(fingerprint, options);
          break;
        case 'custom':
          license = this.generateCustomLicense(fingerprint, options);
          break;
        default:
          throw new Error(`Unknown license type: ${licenseType}`);
      }
      
      licenses.push(license);
    }
    
    return licenses;
  }

  // Export license data
  exportLicenseData(license) {
    return {
      licenseKey: license.licenseKey,
      licenseId: license.licenseData.id,
      type: license.licenseData.type,
      appName: license.licenseData.appName,
      version: license.licenseData.version,
      issuedAt: new Date(license.licenseData.issuedAt).toISOString(),
      expiresAt: license.expiresAt,
      features: license.licenseData.features,
      restrictions: license.licenseData.restrictions,
      metadata: license.licenseData.metadata
    };
  }
